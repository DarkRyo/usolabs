package py.edu.uaa.usolabs.model;

public class Alumno extends Persona {
	
	public String getCod_alumno() {
		return cod_alumno;
	}
	public void setCod_alumno(String cod_alumno) {
		this.cod_alumno = cod_alumno;
	}
	public String getCi_alumno() {
		return ci_alumno;
	}
	public void setCi_alumno(String ci_alumno) {
		this.ci_alumno = ci_alumno;
	}
	public String getNombreYapellido() {
		return nombreYapellido;
	}
	public void setNombreYapellido(String nombreYapellido) {
		this.nombreYapellido = nombreYapellido;
	}
	public String getEdad() {
		return edad;
	}
	public void setEdad(String edad) {
		this.edad = edad;
	}
	public String getCod_carrera() {
		return cod_carrera;
	}
	public void setCod_carrera(String cod_carrera) {
		this.cod_carrera = cod_carrera;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getCod_ciudad() {
		return cod_ciudad;
	}
	public void setCod_ciudad(String cod_ciudad) {
		this.cod_ciudad = cod_ciudad;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getCelular() {
		return celular;
	}
	public void setCelular(String celular) {
		this.celular = celular;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	private String cod_alumno;
	private String ci_alumno;
	private String nombreYapellido;
	private String edad;
	private String cod_carrera;
	private String direccion;
	private String cod_ciudad;
	private String telefono;
	private String celular;
	private String email;

	
	
	
}
